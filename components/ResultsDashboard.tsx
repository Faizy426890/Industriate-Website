// ── Affected area region definitions per photo angle ─────────────────────────
// Each region is a relative ellipse: { cx, cy, rx, ry } in 0..1 of image dims,
// plus the severity key and label.

type Region = {
  cx: number; cy: number;
  rx: number; ry: number;
  key: keyof AnalysisResult['affected_areas'];
  label: string;
};

