function severityColor(pct: number): string {
  if (pct < 20)  return '#14b8a6'; // teal  — negligible
  if (pct < 40)  return '#84cc16'; // lime  — mild
  if (pct < 60)  return '#eab308'; // yellow
  if (pct < 75)  return '#f97316'; // orange
  return '#ef4444';                 // red   — severe
}

// ── Canvas overlay component ──────────────────────────────────────────────────

function AnnotatedImage({
  src,
  angle,
  result,
  active,
}: {
  src: string;
  angle: string;
  result: AnalysisResult;
  active: boolean;
}) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const imgRef    = useRef<HTMLImageElement | null>(null);

  const draw = useCallback(() => {
    const canvas = canvasRef.current;
    if (!canvas || !imgRef.current) return;
    const img = imgRef.current;
    const W = canvas.width  = img.naturalWidth  || canvas.offsetWidth;
    const H = canvas.height = img.naturalHeight || canvas.offsetHeight;
    const ctx = canvas.getContext('2d')!;
    ctx.clearRect(0, 0, W, H);
    ctx.drawImage(img, 0, 0, W, H);

    const regions = ANGLE_REGIONS[angle] || [];

    for (const reg of regions) {
      const pct   = result.affected_areas[reg.key] ?? 0;
      if (pct < 5) continue; // skip negligible

      const color = severityColor(pct);
      const x  = reg.cx * W;
      const y  = reg.cy * H;
      const rx = reg.rx * W;
      const ry = reg.ry * H;

      // Fill ellipse with translucent colour
      ctx.save();
      ctx.beginPath();
      ctx.ellipse(x, y, rx, ry, 0, 0, Math.PI * 2);
      ctx.fillStyle   = color + '40'; // ~25% opacity
      ctx.strokeStyle = color + 'cc';
      ctx.lineWidth   = 2.5;
      ctx.setLineDash([]);
      ctx.fill();
      ctx.stroke();
      ctx.restore();

      // Label pill
      const label = `${reg.label} ${pct}%`;
      ctx.save();
      ctx.font         = `bold ${Math.round(W * 0.032)}px system-ui, sans-serif`;
      ctx.textBaseline = 'middle';
      const tw  = ctx.measureText(label).width;
      const ph  = W * 0.025;
      const pw  = tw + ph * 2;
      const lx  = x - pw / 2;
      const ly  = y - ry - ph * 2.4;

      // Pill background
      ctx.beginPath();
      ctx.roundRect(lx, ly, pw, ph * 2, ph);
      ctx.fillStyle = '#000000cc';
      ctx.fill();

      // Pill border
      ctx.beginPath();
      ctx.roundRect(lx, ly, pw, ph * 2, ph);
      ctx.strokeStyle = color + 'bb';
      ctx.lineWidth   = 1.5;
      ctx.stroke();

      // Text
      ctx.fillStyle = color;
      ctx.fillText(label, lx + ph, ly + ph);
      ctx.restore();

      // Corner dot
      ctx.beginPath();
      ctx.arc(x, y, W * 0.012, 0, Math.PI * 2);
      ctx.fillStyle = color;
      ctx.fill();
    }
  }, [angle, result]);

  useEffect(() => {
    const img  = new Image();
    img.onload = () => { imgRef.current = img; draw(); };
    img.src    = src;
  }, [src, draw]);

  return (
    <div className="relative w-full aspect-[4/3] rounded-2xl overflow-hidden bg-black/40">
      <canvas
        ref={canvasRef}
        className="w-full h-full object-cover"
        style={{ display: 'block' }}
      />
      {/* Angle badge */}
      <div className="absolute top-2 left-2 px-2.5 py-1 rounded-lg bg-black/60 backdrop-blur-sm">
        <span className="text-white/70 text-[10px] font-mono tracking-widest uppercase">{angle} view</span>
      </div>
    </div>
  );
}

// ── Image carousel (mobile‑friendly swipeable tabs) ───────────────────────────

function AnnotatedGallery({ images, result }: { images: CapturedImages; result: AnalysisResult }) {
  const available = (['front', 'left', 'right', 'top'] as const).filter(a => images[a]);
  const [idx, setIdx] = useState(0);

  if (available.length === 0) return null;

  const current = available[idx];

  return (
    <div className="glass rounded-2xl p-4">
      <h3 className="text-white font-semibold mb-3 flex items-center gap-2">
        <div className="w-1.5 h-4 rounded-full bg-plasma" />
        Baldness Area Map
        <span className="ml-auto text-white/30 text-xs font-mono">AI ANNOTATED</span>
      </h3>

      {/* Main annotated image */}
      <AnimatePresence mode="wait">
        <motion.div
          key={current}
          initial={{ opacity: 0, x: 12 }}
          animate={{ opacity: 1, x: 0 }}
          exit={{ opacity: 0, x: -12 }}
          transition={{ duration: 0.22 }}
        >
          <AnnotatedImage
            src={images[current]!}
            angle={current}
            result={result}
            active
          />
        </motion.div>
      </AnimatePresence>

      {/* Tabs */}
      <div className="flex gap-2 mt-3 justify-center">
        {available.map((a, i) => (
          <button
            key={a}
            onClick={() => setIdx(i)}
            className={`flex-1 py-2 rounded-xl text-xs font-mono tracking-wide transition-all ${
              i === idx
                ? 'bg-white/15 text-white'
                : 'bg-white/5 text-white/35 hover:bg-white/10'
            }`}
          >
            {a.toUpperCase()}
          </button>
        ))}
      </div>

      {/* Legend */}
      <div className="flex flex-wrap gap-x-3 gap-y-1.5 mt-3 px-1">
        {[
          { label: 'Low',      color: '#14b8a6', range: '<20%' },
          { label: 'Mild',     color: '#84cc16', range: '20–40%' },
          { label: 'Moderate', color: '#eab308', range: '40–60%' },
          { label: 'High',     color: '#f97316', range: '60–75%' },
          { label: 'Severe',   color: '#ef4444', range: '>75%' },
        ].map(l => (
          <div key={l.label} className="flex items-center gap-1.5">
            <div className="w-2.5 h-2.5 rounded-full" style={{ backgroundColor: l.color }} />
            <span className="text-white/40 text-[10px]">{l.label} <span className="text-white/25">({l.range})</span></span>
          </div>
        ))}
      </div>
    </div>
  );
}

// ── Score ring ────────────────────────────────────────────────────────────────

function ScoreRing({ score, level }: { score: number; level: number }) {
  const cfg  = LEVEL_CONFIG[level] || LEVEL_CONFIG[0];
  const r    = 70;
  const circ = 2 * Math.PI * r;
  const offset = circ * (1 - score / 100);

  return (
    <div className="relative w-48 h-48 mx-auto">
      <svg className="w-full h-full -rotate-90" viewBox="0 0 160 160">
        <circle cx="80" cy="80" r={r} fill="none" stroke="rgba(255,255,255,0.06)" strokeWidth="12" />
        <motion.circle
          cx="80" cy="80" r={r}
          fill="none"
          stroke={cfg.ring}
          strokeWidth="12"
          strokeDasharray={circ}
          strokeDashoffset={offset}
          strokeLinecap="round"
          initial={{ strokeDashoffset: circ }}
          animate={{ strokeDashoffset: offset }}
          transition={{ duration: 1.5, ease: 'easeOut', delay: 0.3 }}
        />
      </svg>
      <div className="absolute inset-0 flex flex-col items-center justify-center">
        <motion.span
          className="font-display text-4xl font-bold"
          style={{ color: cfg.ring }}
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{ delay: 0.5 }}
        >
          {score}
        </motion.span>
        <span className="text-white/40 text-xs font-mono">BALDNESS SCORE</span>
      </div>
    </div>
  );
}

// ── Heatmap bar ───────────────────────────────────────────────────────────────

function HeatmapArea({ label, value, color }: { label: string; value: number; color: string }) {
  return (
    <div className="flex items-center gap-3">
      <span className="text-white/50 text-xs w-24 text-right">{label}</span>
      <div className="flex-1 h-2 bg-white/5 rounded-full overflow-hidden">
        <motion.div
          className="h-full rounded-full"
          style={{ backgroundColor: color }}
          initial={{ width: 0 }}
          animate={{ width: `${value}%` }}
          transition={{ duration: 1, delay: 0.8, ease: 'easeOut' }}
        />
      </div>
      <span className="font-mono text-xs w-8" style={{ color }}>{value}%</span>
    </div>
  );
}

function getAreaColor(value: number) {
  if (value < 25) return '#14b8a6';
  if (value < 50) return '#eab308';
  if (value < 75) return '#f97316';
  return '#ef4444';
}

